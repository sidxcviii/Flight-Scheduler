    private void cancelCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelCustomerButtonActionPerformed
        // TODO add your handling code here:
        
        
        Passenger name = (Passenger)cancelCustomerNameComboBox.getSelectedItem();
        Day day = (Day)cancelCustomerDayComboBox.getSelectedItem();
        
        
        // delete customer from bookings
        List<Booking> b=null;
        try {
            b = Booking.getCustomerDayBooked(name.toString(), day.toDate());
        } catch (SQLException ex) {
            Logger.getLogger(FlightScheduler.class.getName()).log(Level.SEVERE, null, ex);
        }
        Booking[] a= b.toArray(new Booking[b.size()]);
        Booking booking = new Booking();
        
        for(Booking p : a)
        {
            try {
                //deletes the booking
                
                booking.deleteBookingRecord(p.getPassenger(), p.getFlightNumber(), p.getDay());
                int seatsBooked = booking.getSeatsBooked(p.getFlightNumber(),p.getDay());
                Flight flight = new Flight();
                int seatsAvailable = flight.getSeatsAvailable(p.getFlightNumber());
                
                //checks if new entry can be added from waitlist
                if(seatsAvailable>seatsBooked)
                {
                        Waitlist x= new Waitlist();
                        String newName = x.getFirstWaitList(p.getFlightNumber(), p.getDay());
                        booking.addBooking(newName, p.getFlightNumber(), p.getDay());
                }
            } catch (SQLException ex) {
                Logger.getLogger(FlightScheduler.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        //delete booking from waitlist
        List<Waitlist> w=null;
        try {
            w = Waitlist.getCustomerDayWaitlisted(name.toString(), day.toDate());
        } catch (SQLException ex) {
            Logger.getLogger(FlightScheduler.class.getName()).log(Level.SEVERE, null, ex);
        }
        Waitlist[] h= w.toArray(new Waitlist[w.size()]);
        Waitlist waitlist = new Waitlist();
        
        for(Waitlist p : h)
        {
            try {
                waitlist.deleteWaitlistRecord(p.getPassenger(), p.getFlightNumber(), p.getDay());
                waitlist.updateWaitlistPositions(p.getFlightNumber(), p.getDay(),p.getPosition());
            } catch (SQLException ex) {
                Logger.getLogger(FlightScheduler.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_cancelCustomerButtonActionPerformed

