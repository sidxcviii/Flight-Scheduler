    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed
        // TODO add your handling code here:
        Passenger name = (Passenger)bookCustomerComboBox.getSelectedItem();
        Flight flightName = (Flight)bookFlightComboBox.getSelectedItem();
        int result = 0;
        Day day = (Day)bookDayComboBox.getSelectedItem();
        Booking booking = new Booking();
        Waitlist waitlist = new Waitlist();
        int seatsBooked = booking.getSeatsBooked(flightName.toString(),day.toDate());
        
        Flight flight = new Flight();
        int seatsAvailable = flight.getSeatsAvailable(flightName.toString());
        if(seatsAvailable>seatsBooked)
        {
            result = booking.addBooking(name.toString(), flightName.toString(), day.toDate());
            if(result!=0)
            {
                bookCustomerStatusLabel.setText("Status: Flight successfully booked!");
            }
            else
            {
                bookCustomerStatusLabel.setText("Status: Error, Flight not booked!");
            }
        }
        else
        {
            result = waitlist.addBooking(name.toString(), flightName.toString(), day.toDate());
            if(result!=0)
            {
                bookCustomerStatusLabel.setText("Status: Flight full.. But you're waitlisted at position "+result+ "!!");
            }
            else
            {
                bookCustomerStatusLabel.setText("Status: Error, Flight not booked/waitlisted!");
            }
        }
        
    }//GEN-LAST:event_bookButtonActionPerformed

